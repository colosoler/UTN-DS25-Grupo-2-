export const intToAnioString = (int) => {
    switch (int) {
        case 1: return "1ero";
        case 2: return "2do";
        case 3: return "3ro";
        case 4: return "4to";
        case 5: return "5to";
        case 6: return "6to";
        case 7: return "7mo";
        case 8: return "8vo";
        case 9: return "9no";
        default: return "";
    }
}